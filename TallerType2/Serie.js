export class Serie {
    constructor(id, nombre, canal, temporadas, descripcion, linkPagina, imagen) {
        this.id = id;
        this.nombre = nombre;
        this.canal = canal;
        this.temporadas = temporadas;
        this.descripcion = descripcion;
        this.linkPagina = linkPagina;
        this.imagen = imagen;
    }
}
